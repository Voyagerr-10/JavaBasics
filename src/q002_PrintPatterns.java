//2: Print these patterns:
/*      1. Right Half pyramid
        2. Reverse Right Half Pyramid
        3. Left Half Pyramid
        4. Reverse Left Half Pyramid*/

public class q002_PrintPatterns {
    public static void main(String[] args) {

        System.out.println("Pattern 1: Right Half Pyramid");
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");

        System.out.println("Pattern 2: Reverse Right Half Pyramid");
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*");

        System.out.println("Pattern 3: Left Half Pyramid");
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");

        System.out.println("Pattern 4: Reverse Left Half Pyramid");
        System.out.println("* * * * *");
        System.out.println("  * * * *");
        System.out.println("    * * *");
        System.out.println("      * *");
        System.out.println("        *");
    }
}
